package cn.thepetshop.dao;

public class GetAllGoods {
	private int gid;
	private int cid;
	private String gname;
	private double gprice;
	private int gnum;
	private int gsold;
	private float gscore;
	private String gbrief;
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public double getGprice() {
		return gprice;
	}
	public void setGprice(double gprice) {
		this.gprice = gprice;
	}
	public int getGnum() {
		return gnum;
	}
	public void setGnum(int gnum) {
		this.gnum = gnum;
	}
	public int getGsold() {
		return gsold;
	}
	public void setGsold(int gsold) {
		this.gsold = gsold;
	}
	public float getGscore() {
		return gscore;
	}
	public void setGscore(float gscore) {
		this.gscore = gscore;
	}
	public String getGbrief() {
		return gbrief;
	}
	public void setGbrief(String gbrief) {
		this.gbrief = gbrief;
	}
}
